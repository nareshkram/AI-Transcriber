from gtts import gTTS

# 1 मिनट तक का टेक्स्ट (आप अपने अनुसार एडिट कर सकते हैं)
text = """यह एक टेस्ट ऑडियो है। यह ऑडियो 1 मिनट तक चलेगा। 
आप इसे अपने अनुसार बदल सकते हैं और किसी भी टेक्स्ट को स्पीच में कन्वर्ट कर सकते हैं। 
Python का gTTS मॉड्यूल इसे आसान बनाता है।"""

# gTTS ऑब्जेक्ट बनाना
tts = gTTS(text, lang='hi')  # 'hi' हिंदी के लिए है

# ऑडियो फाइल सेव करना
tts.save("output.mp3")

print("ऑडियो सफलतापूर्वक बना और output.mp3 में सेव हुआ!")
